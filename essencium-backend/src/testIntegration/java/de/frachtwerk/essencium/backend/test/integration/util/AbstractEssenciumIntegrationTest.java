package de.frachtwerk.essencium.backend.test.integration.util;

import java.time.Duration;
import org.springframework.test.context.ActiveProfiles;
import org.springframework.test.context.DynamicPropertyRegistry;
import org.springframework.test.context.DynamicPropertySource;
import org.testcontainers.junit.jupiter.Testcontainers;
import org.testcontainers.postgresql.PostgreSQLContainer;
import org.testcontainers.utility.DockerImageName;

@Testcontainers
@ActiveProfiles({"test_postgresql"})
public abstract class AbstractEssenciumIntegrationTest {
  public static final DockerImageName POSTGRES_IMAGE = DockerImageName.parse("postgres:18");

  static final PostgreSQLContainer POSTGRES_CONTAINER = new PostgreSQLContainer(POSTGRES_IMAGE);

  @DynamicPropertySource
  static void configure(DynamicPropertyRegistry registry) {
    if (!POSTGRES_CONTAINER.isRunning()) {
      POSTGRES_CONTAINER.withMinimumRunningDuration(Duration.ofSeconds(5)).start();
    }
    registry.add("spring.datasource.url", POSTGRES_CONTAINER::getJdbcUrl);
    registry.add("spring.datasource.username", POSTGRES_CONTAINER::getUsername);
    registry.add("spring.datasource.password", POSTGRES_CONTAINER::getPassword);
  }
}
